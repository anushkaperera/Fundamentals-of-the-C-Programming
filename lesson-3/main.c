/*
 * main.c
 * 
 * this program calculates are of circle A[m] for given radius, r[m]: A =  PI*r*r.
 *
 */

/*******************************************************************************************************
 * PREPROCESSOR COMMANDS
 *******************************************************************************************************/

#include <stdio.h> // input/output library
#define PI (3.14159265359)

/********************************************************************************************************
 * MAIN FUNCTION ====
 *******************************************************************************************************/

int main()
{
	float A, r;
	r = 10.0f;
	A = PI*r*r;
	printf("Area = %f when r = %f\n", A, r);
	return 0;
}
